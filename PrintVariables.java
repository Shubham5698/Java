package com.Day1;

public class PrintVariables {
    public static void main(String[] args) {
        int i=10;
        String s="Shubham";
        float f=1.457867f;
        double d=5.21899812;
        char c='S';
        boolean bool=true;

        System.out.println("Integer data:"+i+", String data:"+s+", float data:"+f+
                ", double data:"+d+", character data:"+c+", Boolean data:"+bool);
    }
}

